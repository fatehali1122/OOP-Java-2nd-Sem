public class PrimitiveTypes{
	public static void main(String args[]){

		byte b=100;	
		short s =5;
		int i=10;
		long l=300;

		float f=10.0f;
		double d= 400.0;

		char ch='c';
		boolean bool = true; // false
		System.out.printf("%50s","Primitive Types Understanding task\n");
	
		System.out.println("Value in int:"+i);

		System.out.printf("value in int :%03d\n", i);
		System.out.printf("Value in short:%d\n",s);
		System.out.printf("Value in byte:%d\n",b);
		System.out.printf("Value in long:%d\n",l);	
		System.out.printf("Value in float:%f\n",f);
		System.out.printf("Value in double:%f\n",d);
		System.out.printf("Value in boolean:%b\n",bool);
		System.out.printf("Value in char:%c\n",ch);

		System.out.printf("%03d\t%10s\t%.2f\n",1,"Fateh Ali",3.86);
		





}
}